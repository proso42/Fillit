#include <string.h>

void	*ft_memset(void *b, int c, size_t len)
{
	char	*chaine;
	size_t	i;

	if (len > 0)
	{
		i = 0;
		chaine = b;
		while (i < len)
		{
			chaine[i] = c;
			i++;
		}
	}
	return (b);
}
